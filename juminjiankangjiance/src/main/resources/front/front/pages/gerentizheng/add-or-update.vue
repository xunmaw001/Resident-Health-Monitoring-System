            async onSubmitTap() {
				if ((!this.ruleForm.gerentizhengName)) {
                    this.$utils.msg(`标题不能为空`);
                    return
                }
				if ((!this.ruleForm.gerentizhengXieya) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengXieya)) && this.ruleForm.gerentizhengXieya >0) {
                    this.$utils.msg(`血压不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengXietang) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengXietang)) && this.ruleForm.gerentizhengXietang >0) {
                    this.$utils.msg(`血糖不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengShengao) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengShengao)) && this.ruleForm.gerentizhengShengao >0) {
                    this.$utils.msg(`身高不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengTizhong) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengTizhong)) && this.ruleForm.gerentizhengTizhong >0) {
                    this.$utils.msg(`体重KG不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengTiwen) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengTiwen)) && this.ruleForm.gerentizhengTiwen >0) {
                    this.$utils.msg(`体温不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengBushu) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengBushu)) && this.ruleForm.gerentizhengBushu >0) {
                    this.$utils.msg(`步数不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengShilil) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengShilil)) && this.ruleForm.gerentizhengShilil >0) {
                    this.$utils.msg(`视力L不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengShilir) && (!this.$validate.isIntNumer(this.ruleForm.gerentizhengShilir)) && this.ruleForm.gerentizhengShilir >0) {
                    this.$utils.msg(`视力R不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.gerentizhengTime)) {
                    this.$utils.msg(`记录时间不能为空`);
                    return
                }
				if ((!this.ruleForm.gerentizhengText)) {
                    this.$utils.msg(`备注不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`gerentizheng`, this.ruleForm);
                } else {
                    await this.$api.save(`gerentizheng`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
